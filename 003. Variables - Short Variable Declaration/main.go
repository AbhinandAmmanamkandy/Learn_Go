package main

import "fmt"

func main() {

	// sad variable declaration
	var mySkillIssues int
	mySkillIssues = 42
	fmt.Println(mySkillIssues)

	// Walrus operator method (Short Variable Declaration)
	ghost := 99
	fmt.Println(ghost)

	// int
	number := 42
	fmt.Println(number)

	// float64
	pi := 3.14159
	fmt.Println(pi)

	// string
	message := "Hello World"
	fmt.Println(message)

	// bool
	isGoat := true
	fmt.Println(isGoat)

	messageStart := "Happy birthday! You are now"
	age := 21
	messageEnd := "years old!"
	fmt.Println(messageStart, age, messageEnd)

}
